public class Criaçao {
    private String nome_cri;
    private String desc;

    public Criaçao(String n, String d) {
        this.nome_cri = n;
        this.desc = d;
    }

    public String getNomeCri() {
        return nome_cri;
    }

    public String getDescricao() {
        return desc;
    }
}
